# Dog class
class Dog:
    # Method of the class
    def bark(self):
        print("Ham-Ham")
#Create a new instance
charle = Dog()
# call the method
charle.bark()
# This will output "Ham-Ham"
# In Python, methods are functions that are defined as part of a class. it is common practice that the first argument of any method that is part of a class is the actual object calling method. This argument is usually called self.
